#include <bits/stdc++.h>
#define endl "\n"
using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    vector<int> v;
    int x;
    while (cin >> x) {
        v.push_back(x);
    }
    for (int i = v.size() - 1; i >= 0; i--) {
        cout << v[i] << endl;
    }
    return 0;
}

